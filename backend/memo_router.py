    """메모를 '확인' 처리합니다."""
    memo = db.query(ApprovalLog).filter(ApprovalLog.id == memo_id, ApprovalLog.company_id == current_user.company_id).first()
    if not memo:
        raise HTTPException(status_code=404, detail="Memo not found")

    fact = memo.fact_candidate
    if not (current_user.role_code == RoleCode.tenant_admin or fact.assigned_user_id == current_user.id):
        raise HTTPException(status_code=403, detail="Not authorized to acknowledge this memo")

